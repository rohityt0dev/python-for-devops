# Integer Type
age = 25
port = 8080

print (age)
print (port)

# String Type
name = "Rohit"
city = "Mumbai"
Cloud = "AWS"

print (name)
print (city)
print (Cloud)


# Float Type
height = 5.1
weight = 67

print (height)
print (weight)

# Boolean Type
server_running = True
deployment_success = False

print (server_running)
print (deployment_success)


# None Type
status = None

print (status)